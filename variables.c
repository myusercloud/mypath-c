#include <stdio.h>

int main() {
    printf("Let us learn about variables in C!\n");

    int age = 21;
    double height = 5.9;
    char name[] = "Harry";

    printf("My name is %s\n", name);
    printf("I am %d years old and I'm %.1f feet tall\n", age, height);
    printf("Variables are used to store data in a program.\n");

    return 0;
}
